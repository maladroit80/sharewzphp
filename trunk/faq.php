        <h4>4.赚钱相关问题</h4>
        <ul>
          <li><strong>问：怎样赚取现金？</strong><br />
            答：目前您可以通过点击广告、参加注册项目获取现金奖励。今后我们还会推出更多的赚现金方法。 </li>
          <li><strong>问：你们有下线提成吗？</strong><br />
            答：是的，我们有1层下线体系，您可以从您的第一层下线获得50%的提成，这些提成是系统额外给您的奖励，并不会影响您的下线的收入。 </li>
          <li><strong>问：我怎么可以得到下线？</strong><br />
            答：登陆您的帐户后，在“我的账户”里可以看见您的下线连接，通过此连接注册的会员自动成为您的下线。</li>
          <li><strong>问：什么是“活动的有效直接下线”？</strong><br />
            答：“活动的有效直接下线”，必须满足以下条件：下线帐号已激活、下线帐号当前有效、下线帐号当前处于活动状态</li>
          <li><strong>问：每个点击任务只能完成一次吗？</strong><br />
            答：我们没有限制，每个广告每24小时只加一次钱。 </li>
          <li><strong>问：我可以在上一个广告没有完成之前就点击下一个广告吗？</strong><br />
            答：不可以。为了广告商的利益必须使广告页停留一定时间。 </li>
        </ul>
