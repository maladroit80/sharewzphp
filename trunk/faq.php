        <h4>2.账户相关问题</h4>
        <ul>
          <li><strong>问：注册时收费吗？</strong><br />
            答：注册时我们不仅不收费，还会给予注册奖励。如果您需要升级为星级会员，就需要交一定的费用，可它是自愿的，如果您不想升级成为星级会员，则不需要交任何费用。 </li>
          <li><strong>问：我一个人能够注册多个帐户吗？</strong><br />
            答：对不起，每个人只能有一个帐户。如果发现有多帐户来自同一IP地址的情况，我们将在不预先通知的情况下直接删除帐户。 </li>
          <li><strong>问：我可以修改个人信息和密码吗？</strong><br />
            答：完全可以，您登陆后就可以看见修改个人资料和密码，进入后自行修改即可。</li>
        </ul>
